/// Attributes for a `spawn_cube` call.
#[derive(Reflect, Debug, Clone)]
pub(crate) struct SpawnInfo {
    /// Offset to spawn relative to the caller's position.
    pub offset: Vec3,
    /// Size of object, in m.
    pub size: Vec3,
    /// Direction the object should be oriented. Default is Vec3::ZERO.
    pub angle: Vec3,
    pub color: Color,
    pub mass: f32,
    pub is_static: bool,
    /// The rules for the item.
    pub life_policy: SpawnedLifetimePolicy,
    pub has_synth: bool,
}

/// Policy for the object(s) generated by the `spawn_cube` call.
#[derive(Debug, Default, Clone, Copy, PartialEq)]
#[derive(Reflect)]
#[type_path = "game"]
pub enum SpawnedLifetimePolicy {
    /// Create as a temporary object, which will be despawned
    /// after a given amount of game time.
    Temporary{ secs: f32 },
    /// (Try to) keep the object.
    /// This is typically bounded by the current level's end.
    #[default]
    Keep,
}

impl Default for SpawnInfo {
    fn default() -> Self {
        Self {
            offset: Vec3::ZERO,
            size: Vec3::splat(1.0),
            color: Color::WHITE,
            angle: Vec3::ZERO,
            mass: 1.0,
            is_static: false,
            life_policy: SpawnedLifetimePolicy::Keep,
            has_synth: false,
        }
    }
}

pub(crate) fn spawn_cube(In((entity, rt, args)): In<(Entity, Arc<Runtime>, Vec<ObjectPtr>)>, world: &mut World) -> ExecResult {
    if args.is_empty() {
        return Err(RuntimeError::MissingArgument(0));
    }

    let xfrm = {
        let mut query = world.query::<&Transform>();
        query.get(world, entity).map(|x| *x).unwrap_or_default()
    };
    let world_entity = {
        let world_entity = world.get_resource::<WorldMarkerEntity>().ok_or(RuntimeError::LiteralError(format!("no WorldMarkerEntity")))?;
        world_entity.0
    };
    let info = {
        let type_regy = world.get_resource::<AppTypeRegistry>().ok_or(RuntimeError::LiteralError(format!("no AppTypeRegistry")))?;
        let assets = world.get_resource::<AssetServer>().ok_or(RuntimeError::LiteralError(format!("no AssetServer")))?;
        let ctx: ConvertContext = ConvertContext::new(&rt, &type_regy, &assets);
        convert_obj_to_value::<SpawnInfo>(&ctx, &args[0])
            .map_err(|e| RuntimeError::LiteralError(format!("{e}")))?
    };

    let mut size = info.size;

    let (mesh, mat, collider) = {
        let models = world.get_resource::<ModelAssets>().ok_or(RuntimeError::LiteralError(format!("no ModelAssets")))?;
        let gmesh = {
            let meshes = world.get_resource::<Assets<GltfMesh>>().ok_or(RuntimeError::LiteralError(format!("no Assets<GltfMesh>")))?;
            meshes.get(&models.tile).ok_or(RuntimeError::LiteralError(format!("no GltfMesh")))?
        };
        let prim = gmesh.primitives[0].clone();

        let mat = gmesh.primitives[0].material.clone();

        let mat = {
            let mut materials = world.get_resource_mut::<Assets<StandardMaterial>>().ok_or(RuntimeError::LiteralError(format!("no Assets<StandardMaterial>")))?;
            match mat.as_ref() {
                Some(mat) => {
                    let mut std_mat = materials.get(mat).unwrap().clone();
                    std_mat.base_color = info.color.mix(&Color::WHITE, 0.75);
                    // std_mat.specular_tint = info.color.mix(&Color::WHITE, 0.25).with_alpha(0.5);
                    // std_mat.specular_tint = info.color.mix(&Color::WHITE, 0.25); //.with_alpha(0.5);
                    materials.add(std_mat)
                },
                None => {
                    materials.add(StandardMaterial {
                        base_color: info.color,
                        perceptual_roughness: 0.75,
                        ..default()
                    })
                }
            }
        };

        let collider = Collider::cuboid(1., 0.025, 1.);

        size.y = size.x.min(size.z);

        (prim.mesh.clone(), mat, collider)
    };

    let new_xfrm = Transform::from_translation(info.offset + xfrm.translation)
        .with_rotation(
            xfrm.rotation *
            Quat::from_euler(
                default(),
                info.angle.x.to_radians(),
                info.angle.y.to_radians(),
                info.angle.z.to_radians())
        )
        .with_scale(size.adjust_precision())
    ;

    let mut queue = CommandQueue::default();
    let mut commands = Commands::new(&mut queue, world);

    let id = commands.spawn((
        (
            ChildOf(world_entity),
            Name::new("SCRIPTED CUBE"),
            Cube,
            Spawned,
            CrosshairTargetable,
            Mesh3d(mesh),
            MeshMaterial3d(mat),
            new_xfrm,
        ),
        (
            if info.is_static { RigidBody::Static } else { RigidBody::Dynamic },
            collider,
            Restitution::new(0.0),
            Friction::new(1.0),
            SleepThreshold {
                linear: 1.0 / 16.0,
                angular: 1.0 / 16.0,
            },
            LinearDamping(1.0 / 16.0),
            AngularDamping(1.0 / 16.0),
            Mass(if info.mass <= 0.0 { 1.0 } else { info.mass }),
            CollisionMargin(0.0025),
        ),
        CollisionLayers::new(
            GameLayer::Projectiles,
            [
                GameLayer::Default,
                GameLayer::World,
                GameLayer::Player,
                GameLayer::Projectiles,
            ],
        ),
        DespawnAfter(match info.life_policy {
            SpawnedLifetimePolicy::Keep => Duration::ZERO,
            SpawnedLifetimePolicy::Temporary { secs } => Duration::from_secs_f32(secs),
        })
    )).id();

    if info.has_synth {
        commands.entity(id).insert(OurMidiSynth);
    }

    queue.apply(world);

    Ok(ExecState::Result(rt.for_typed_uint(&rt.types.u64_type, id.to_bits() as usize)?))
}

pub(crate) fn send_midi_message(In((entity, rt, args)): In<(Entity, Arc<Runtime>, Vec<ObjectPtr>)>,
    mut commands: Commands,
    ctx: ConvertContextParam,
) -> ExecResult {
    if args.len() < 1 {
        return Err(RuntimeError::MissingArgument(0));
    }

    let delay = {
        if args.len() >= 2 {
            let Some(delay) = RtNumber::new(&args[1]) else {
                return Err(RuntimeError::LiteralError(format!("expected a number for the delay argument, got {}",
                    RtDisplay::new(&rt, &args[1]))));
            };
            if delay.as_real() < 0.0 {
                return Err(RuntimeError::LiteralError(format!("delay cannot be negative, got {}",
                    RtDisplay::new(&rt, &args[1]))));
            }
            delay.as_real() as f32
        } else {
            0.0f32
        }
    };


    let command = convert_obj_to_value::<SynthCommand>(
        &ctx.as_ctx(), &args[0])
    .map_err(|e| RuntimeError::LiteralError(format!("{e}")))?;

    let message = SynthMessage::new(entity, command).after_secs(delay);
    commands.write_message(message);

    Ok(ExecState::Result(ObjectPtr::for_nil()))
}

pub(crate) fn add_script(In((entity, rt, args)): In<(Entity, Arc<Runtime>, Vec<ObjectPtr>)>,
    ctx_p: ConvertContextParam,
    mut pending: ResMut<PendingScripts>,
) -> ExecResult {
    if args.len() < 1 {
        return Err(RuntimeError::MissingArgument(0));
    }

    let ctx: ConvertContext = ctx_p.as_ctx();
    let script_info = convert_obj_to_value::<ScriptCreationInfo>(
        &ctx, &args[0])
        .map_err(|e| RuntimeError::LiteralError(format!("{e}")))?;

    let handle = ctx.assets.load(&script_info.path);

    pending.insert(PendingScript {
        target: entity,
        handle,
        info: script_info,
    });

    Ok(ExecState::Result(ObjectPtr::for_nil()))
}

pub(crate) fn remove_script(In((entity, _rt, args)): In<(Entity, Arc<Runtime>, Vec<ObjectPtr>)>,
    world: &mut World,
) -> ExecResult {
    if args.len() < 1 {
        return Err(RuntimeError::MissingArgument(0));
    }

    world.get_entity_mut(entity)
        .map_err(|e| RuntimeError::LiteralError(format!("failed to remove script: {e}")))?
        .remove::<Script<UserScript>>();

    Ok(ExecState::Result(ObjectPtr::for_nil()))
}

pub(crate) fn translate(In((entity, rt, args)): In<(Entity, Arc<Runtime>, Vec<ObjectPtr>)>,
    ctx: ConvertContextParam,
    mut xfrm_q: Query<&mut Transform>,
) -> ExecResult {
    if args.len() < 1 {
        return Err(RuntimeError::MissingArgument(0));
    }

    let offset = convert_obj_to_value::<Vec3>(&ctx.as_ctx(), &args[0])
        .map_err(|e| RuntimeError::LiteralError(format!("{e}")))?;

    let mut xfrm = xfrm_q.get_mut(entity)
        .map_err(|e| RuntimeError::LiteralError(format!("{e}")))?;

    xfrm.translation += offset;

    Ok(ExecState::Result(ObjectPtr::for_nil()))
}

pub(crate) fn add_velocity(In((entity, rt, args)): In<(Entity, Arc<Runtime>, Vec<ObjectPtr>)>,
    mut commands: Commands,
    ctx: ConvertContextParam,
    mut vel_q: Query<&mut LinearVelocity>,
) -> ExecResult {
    if args.len() < 1 {
        return Err(RuntimeError::MissingArgument(0));
    }

    let new_vel = convert_obj_to_value::<Vec3>(&ctx.as_ctx(), &args[0])
        .map_err(|e| RuntimeError::LiteralError(format!("{e}")))?;

    let mut vel = vel_q.get_mut(entity)
        .map_err(|e| RuntimeError::LiteralError(format!("{e}")))?;

    vel.0 = vel.0 + new_vel.adjust_precision();

    commands.entity(entity).try_remove::<Sleeping>();
    Ok(ExecState::Result(ObjectPtr::for_nil()))
}

pub(crate) fn set_gravity(In((entity, rt, args)): In<(Entity, Arc<Runtime>, Vec<ObjectPtr>)>,
    mut commands: Commands,
    ctx: ConvertContextParam,
    mut gravity: ResMut<Gravity>,
    gravity_q: Query<&GravityScale>,
) -> ExecResult {
    if args.len() < 1 {
        return Err(RuntimeError::MissingArgument(0));
    }

    let ctx: ConvertContext = ctx.as_ctx();
    let old_vel = if entity == Entity::PLACEHOLDER {
        log::info!("global gravity");
        let old_vel = gravity.0;
        if let Ok(new_vel) = convert_obj_to_value::<Vector>(&ctx, &args[0]) {
            gravity.0 = new_vel;
        } else if let Ok(new_vec) = convert_obj_to_value::<Scalar>(&ctx, &args[0]) {
            gravity.0 = Vector::new(0., new_vec, 0.);
        } else {
            return Err(RuntimeError::LiteralError(format!("expected Vec3 or Real, got {}", RtDisplay::new(&rt, &args[0]))))?;
        }
        old_vel
    } else {
        log::info!("entity gravity {entity}");

        let old_vel = gravity_q.get(entity).map_or(gravity.0, |g| Vector::new(0.0, g.0, 0.0));
        if let Ok(new_vel) = convert_obj_to_value::<Vector>(&ctx, &args[0]) {
            commands.entity(entity).insert(GravityScale(new_vel.y / -9.81));
            gravity.0 = new_vel;
        } else if let Ok(new_vec) = convert_obj_to_value::<Scalar>(&ctx, &args[0]) {
            commands.entity(entity).insert(GravityScale(new_vec / -9.81));
            gravity.0 = Vector::new(0., new_vec, 0.);
        } else {
            return Err(RuntimeError::LiteralError(format!("expected Vec3 or Real, got {}", RtDisplay::new(&rt, &args[0]))))?;
        }
        old_vel
    };

    let mut fields = FxHashMap::default();
    for (k, v) in [
        (rt.pool.for_str("x"), ObjectPtr::for_real(old_vel.x as TypeReal)?),
        (rt.pool.for_str("y"), ObjectPtr::for_real(old_vel.y as TypeReal)?),
        (rt.pool.for_str("z"), ObjectPtr::for_real(old_vel.z as TypeReal)?),
    ] {
        fields.insert(k, v);
    }
    let struc = fedry_runtime::prelude::Struct::from_fields(fields);
    Ok(ExecState::Result(
        ObjectPtr::for_struct(&rt.memory, &rt.types.struct_type, struc)?
    ))
}
